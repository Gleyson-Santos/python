"""
Operadores Relacionais
== > >= < <= !=
"""
num_1: int = 2
num_2: int = 2
var1 = "Luiz"
var2 = "Otavio"

expressao1 = num_1 == num_2
expressao2= num_1 > num_2
expressao3 = num_1 >= num_2
expressao4 = num_1 < num_2
expressao5 = num_1 <= num_2
expressao6 = num_1 != num_2
expressao7 = var1 != var2

print(f"1", expressao1)
print(f"2", expressao2)
print(f"3", expressao3)
print(f"4", expressao4)
print(f"5", expressao5)
print(f"6", expressao6)
print(f"var", expressao7)
